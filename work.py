# user number inputs
num1 = int(input("Enter the first number:"))
num2 = int(input("Enter the second number: "))

# find sum
sum = (num1 + num2)

# find average
average = (num1 +num2) / 2

# print sum
print("The sum of", (num1), "and", (num2), "is:", sum)

# print average
print("The average of", (num1), "and", (num2), "is: %0.2f" %average)